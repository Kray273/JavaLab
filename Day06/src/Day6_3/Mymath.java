package Day6_3;

public class Mymath {
	long add (long a, long b) {
		long result = a + b;
		return result;
		// return a + b; 한줄로 할 수 있음 
	}
	
	long subtract (long a, long b) {
		return a - b; 
	}
	long multiply (long a, long b) {
		return a * b; 
	}
	double divide (double a, double b) {
		return a /b; 
	}

		
}
