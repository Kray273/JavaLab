public class Day3_5 {
    //가로 세로 출력 중첩 for문 (1개면 :i, 2개면 i,j, 3개면 i,j,k)
    public static void main(String[] args) {
        for(int i=1; i <= 5; i++) {
            for(int j=1; j<=10; j++) {
                System.out.print("*");   
            }
            System.out.println(); //줄바꿈 
        }
        
    
            
    }
}
