// 입력받아 카운트 다운 


import java.util.Scanner;
public class Day4_5 {
    public static void main(String[] args) {
        Scanner stdIn = new Scanner(System.in);
        System.out.println("Count : ");
        int input = stdIn.nextInt();
        

        for(int i=input; i>=0; i--) {
            System.out.println(i);
        } 
        
       

        }
}