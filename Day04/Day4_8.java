package Day4;

//while 문 조건을 만족하면 수행, 만족하지 못하는 순간 빠져나간다.

public class Day4_8 {
    public static void main(String[] args) {

        for(int i=1; i<=10; i++) {
            System.out.printf("%d ", i);
        } System.out.println("");


        int i = 1;
        while(i<=10) {
            System.out.printf("%d ", i);
            i++;
        }System.out.println("");





    }
}
